@vampytest._(vampytest.call_from(_iter_options()).named_first().returning_last())
def test__produce_ip_v6_string(ip_value):
    """
    Tests whether ``produce_ip_v6_string`` works as intended.
    
    Parameters
    ----------
    ip_value : `int`
        Ip value.
    
    Returns
    -------
    output : `str`
    """
    output = [*produce_ip_v6_string(ip_value)]
    for element in output:
        vampytest.assert_instance(element, str)
    
    return ''.join(output)
